<h2><ins>Transmision de creepypastas:</ins></h2>


<h3>Imagenes encantadas</h3>
<p>
Las <b>imágenes encantadas</b> son muchas veces editadas o se tratan de fotos trucadas con el fin de crear historias de ficción y se supone que la imagen perseguirá y atormentará al que la visualice o no cumpla con las condiciones establecidas de dicha imagen.
</p>


<h3>Episodios perdidos</h3>
<p>
El lector a veces queda perturbado por los dichos <b>episodios perdidos</b> son historias de ficción sobre episodios descartados de series de televisión, generalmente cartoons. Los episodios perdidos tienen la intención de poner nervioso al lector desviando la conducta usual de los caracteres, enfocándose en la muerte, e incluyendo imágenes hiper-realistas de los personajes de la serie.
</p>


<h3>Rituales de creepypastas</h3>
<p>
Los <b>rituales</b> tambien son creepypastas muy circulados en internet sobre procedimientos para invocar entidades o tener experiencias sobrenaturales. Muchos usuarios de la web o los conocidos como "Youtubers" suelen hacerlos para comprobar su veracidad o simplemente experimentar un momento de terror en la vida real.
</p>

