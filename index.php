<p>
<acronym title="Hombre delgado">SLENDERMAN</acronym>:
</p>
<p>
<b class="destacado">Slenderman</b> es un personaje ficticio que se originó como un creepypasta en los foros del portal especializado <em>Something Awful</em> en 2003. Es representado con apariencia de un hombre delgado, anormalmente alto y vestido de traje. No posee ningún rasgo facial, tan solo un rostro blanco. Las historias y leyendas urbanas generadas en torno al personaje lo definen como un <dfn title="Concepto filosófico que remite a lo que es, existe o puede existir">ente</dfn> fantasmagórico/espectral que acecha, acosa, perturba y traumatiza a sus víctimas, especialmente niños.
</p>
<p class="cita">
Como dice el creador <cite>Victor Surge</cite>:
</p>
<p class="cita">
No tiene rostro, es delgado, muy alto y viste de negro. Puede aumentar su tamaño, alargar sus brazos y piernas o sacar tentáculos de la espalda. Acecha a sus víctimas por largos períodos, invisibilizándose a voluntad para pasar inadvertido. Suele inducir trastornos mentales y no se sabe cómo mueren sus víctimas:  Simplemente... desaparecen.
</p>

